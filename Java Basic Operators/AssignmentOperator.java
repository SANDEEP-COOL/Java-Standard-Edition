public class AssignmentOperator {
    public static void main(String[] args) {
        
        int a = 7;
        System.out.println("a+= : "+(a+= 3));
        System.out.println("a = "+a);
        System.out.println("a-= : "+(a-= 2));
        System.out.println("a = "+a);
        System.out.println("a*= : "+(a*= 1));
        System.out.println("a = "+a);
        System.out.println("a/= : "+(a/= 7));
        System.out.println("a = "+a);
        System.out.println("a%= : "+(a%= 3));
        System.out.println("a = "+a);

    }
}
